let currentLanguage = 'pt'; // Default language

function setLanguage(lang) {
    if (!translations[lang]) {
        console.warn(`Language "${lang}" not found. Falling back to default.`);
        lang = 'pt';
    }
    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);
    translatePage();
    
    // Atualizar seletor de idioma se existir
    const langSelector = document.getElementById('langSelector');
    if (langSelector) langSelector.value = lang;
}

function translatePage() {
    // Traduzir elementos com data-i18n-key
    document.querySelectorAll('[data-i18n-key]').forEach(element => {
        const key = element.getAttribute('data-i18n-key');
        const translation = translations[currentLanguage][key];
        if (translation) {
            if (element.tagName === 'INPUT' && element.placeholder !== undefined) {
                element.placeholder = translation;
            } else if (element.tagName === 'INPUT' || element.tagName === 'BUTTON') {
                element.value = translation;
            } else {
                element.innerText = translation;
            }
        }
    });
    
    // Traduzir placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        const translation = translations[currentLanguage][key];
        if (translation) element.placeholder = translation;
    });
    
    // Traduzir títulos (tooltips)
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        const translation = translations[currentLanguage][key];
        if (translation) element.title = translation;
    });
}

function t(key) {
    return translations[currentLanguage]?.[key] || translations['pt']?.[key] || key;
}

function getTranslation(key) {
    return t(key);
}

// Load saved language on startup
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage');
    
    // Sincronizar seletor de idioma se existir
    const langSelector = document.getElementById('langSelector');
    if (langSelector) {
        langSelector.value = savedLang || 'pt';
    }
    
    if (savedLang) {
        setLanguage(savedLang);
    } else {
        // Optional: auto-detect browser language
        const browserLang = navigator.language.split('-')[0];
        if (translations[browserLang]) {
            setLanguage(browserLang);
        } else {
            translatePage(); // Translate with default
        }
    }
});
